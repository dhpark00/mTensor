(* inverse permutation of CyclesPhased *)
InversePerm[x_?NumericQ perm_] := 1/x InversePerm[perm]
InversePerm[{cycl_Cycles, s_}] := {InversePermutation[cycl], 1/s}
InversePerm[Imag[ns___]]       := Imag @@ Ordering[{ns}]

(* gives the CyclesSigned permutation that converts lst1 into lst2. *)
PermFromTo::diff = "`1` and `2` contain different points"
PermFromTo[lst1:head_[___], lst2:head_[___]] :=
    If [Sort[lst1] =!= Sort[lst2],
        Message[PermFromTo::diff, lst1, lst2],
    (* else *)
        {FindPermutation[lst1, lst2], +1}
    ]

(* gives a canonical representative of the double coset S.perm.D,
   where S is generated by GS and D is the group of symmetries of the DummySets. *)
Options[CanonicalPerm] = {Verbose -> False}
CanonicalPerm[perm_:{_Cycles, +1 | -1}, len_, GS_GenSet, frees_List, sets:{(_DummySet | _RepeatedSet)...}, opts___Rule] :=
    linkCanonicalPerm[perm, len, SchreierSims[GS], frees, sets, opts]
CanonicalPerm[s_. * imag_Imag, len_, GS_GenSet, frees_List, sets:{(_DummySet | _RepeatedSet)...}, opts___Rule] :=
    ToImag[CanonicalPerm[ToCycl[s * imag], len, GS, frees, sets, opts], len] /; s^2 === 1

