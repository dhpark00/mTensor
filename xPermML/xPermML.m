(* returns the group generated by GS of CyclesSigned *)
Options[MakePermGroup] = {MMALink -> False}
MakePermGroup[GenSet[],  ___]                      := {}
MakePermGroup[GS_GenSet,              opts___Rule] := MakePermGroup[GS, permDeg @ GS, opts]
MakePermGroup[GS_GenSet, len_Integer, opts___Rule] :=
    If [MMALink /. {opts} /. Options[MakePermGroup],  (* True for MMALink mode, False for Math mode *)
        linkMakePermGroup[GS, len],
    (* else *)
        toCyclesSigned[#, len]& /@ GroupElements[gensetToPermGroup[GS, len]]
    ]

(* yields the image of point p under a perm *)
OnPoints[lst_List,  perm_] := OnPoints[#, perm]& /@ lst  (* Listable *)

(* In MMA, PermutationReplace[p, Cycles[{...}] *)
OnPoints[p_Integer, {Cycles[{___List, {___, p_, q_, ___}, ___List}], _}] := q
OnPoints[p_Integer, {Cycles[{___List, {q_, ___, p_},      ___List}], _}] := q
OnPoints[p_Integer, {Cycles[{___List}], _}]                              := p

OnPoints[p_Integer, s_. * perm_Imag]         := OnPoints[p, perm]  (* neglecting the sign of permutations *)
OnPoints[p_Integer, imag:Imag[___, p_, ___]] := imag[[p]]
OnPoints[p_Integer, Imag[___]]               := p

(* returns the sorted orbits of the pts under the action of the group generated by GS of CyclesSigned *)
Orbits[pts_List, GS_GenSet, len_Integer] := GroupOrbits[gensetToPermGroup[GS, len], pts]

PermMemberQ::invalid = "`1` is not a valid `2`."
PermMemberQ[perm_:{_Cycles, _Integer}, len_Integer, GS_GenSet, opts___Rule] :=
    If [len < Max[permDeg @ perm, permDeg @ GS],
        Message[PermMemberQ::invalid, len, "length"]; False,
    (* else *)
        GroupElementQ[gensetToPermGroup[GS, len], toCyclesProper[perm, len]]
    ]
PermMemberQ[s_. * imag_Imag, len_Integer, GS_GenSet, opts___Rule] := PermMemberQ[ToCycl[s * imag], len, GS, opts]

(* permutes the points of a list according to the perm *)
PermuteList[argL_List, {cycl_Cycles, _}] := Permute[argL, cycl]
PermuteList[argL_List, s_. * imag_Imag]  := PermuteList[argL, List @@ imag]
PermuteList[argL_List, imagL_List]       := argL[[ Ordering[imagL] ]]

(* generates a strong generating set for the group generated by GS of CyclesSigned *)
SchreierSims[GenSet[],  ___]         := StrongGenSet[{}, GenSet[]]
SchreierSims[GS_GenSet, opts___Rule] :=
    Module[{len = permDeg[GS], chainL, base},
        (* A possible option is GroupActionBase -> base_List *)
        chainL = GroupStabilizerChain[gensetToPermGroup[GS, len], opts];
        base = Select[chainL[[-1,1]], (# <= len)&];  (* consider perm's sign *)
        StrongGenSet[base, GenSet @@ (toCyclesSigned[#, len]& /@ chainL[[1,-1,1]])]
    ]

(* GenSet of CyclesSigned -> PermutationGroup *)
gensetToPermGroup[GenSet[scycl___], len_Integer] := PermutationGroup[ toCyclesProper[#, len]& /@ {scycl} ]

(* CyclesSigned -> Cycles *) (* Note: NOT CyclesPhased -> Cycles *)
toCyclesProper[{Cycles[cycl_], +1}, len_Integer] := Cycles[cycl]
toCyclesProper[{Cycles[cycl_], -1}, len_Integer] := Cycles @ Append[cycl, len + {1,2}]

(* Cycles -> CyclesSigned *)
toCyclesSigned[Cycles[cycl_], len_Integer] :=
    {Cycles @ DeleteCases[cycl, {___, len + 1, ___}], If [OnPoints[len + 1, {Cycles[cycl], +1}] === len + 1, +1, -1]}

