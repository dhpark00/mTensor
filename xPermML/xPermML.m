(* CyclesSigned --> Imag --> Unsigned imag-list *)
toImagList[GS_GenSet, len_] := Flatten @ Apply[List, toImagList[#, len]& /@ GS]
toImagList[perm_, len_]     := toImagListProper[ToImag[perm, len], len]

    (* Imag --> Unsigned imag-list *)
    toImagListProper[s_. * Imag[ns___], len_] := If[s < 0, Join[{ns}, len + {2,1}], Join[{ns}, len + {1,2}]]

(* Unsigned imag-list --> CyclesSigned *)
fromImagList[{(0)..},    _]          := 0  (* for CanonicalPerm *)
fromImagList[imag_List, len_Integer] := {PermutationCycles @ Drop[imag, -2], If [OnPoints[len+1, Imag @@ imag] === len+1, 1, -1]}

linkCanonicalPerm[perm_, len_Integer, sgs_, frees_List, sets_, opts___] :=
    With[{vb = Verbose /. {opts} /. Options[CanonicalPerm], callF = mTensor`xPermML`Private`MLCanonicalPerm},
        If [vb,
            Print["Calling 'MLCanonicalPerm' with the following arguments:"];
            Print["1st: perm = ", toImagList[perm, len]];
            Print["2nd: deg = ", len + 2];
            Print["3rd: {base, GS} = ", List @ sgsList[sgs, len]];
            Print["4th: freeps = ", frees];
            Print["5th: vds, dummies, mQ, vrs, repes = ", List @ translateSet[sets]];
        ];

        xpermConnect[];
        fromImagList[
            callF[
                toImagList[perm, len],
                len + 2,
                sgsList[sgs, len],
                frees,
                translateSet[sets]
            ],
            len
        ]
    ]

    sgsList[StrongGenSet[base_List, GS_GenSet], len_] := Sequence[takeNext[base, {}, Range[len]], toImagList[GS, len]]

        takeNext[{a_, other___}, {found___}, lst_List] :=
            With[{sublist = Complement[Select[lst, # < a&], {other}]},
                takeNext[{other}, Flatten[{found, sublist, a}], Complement[lst, sublist, {a}]]
            ]
        takeNext[{}, found_, lst_List] := Join[found, lst]

    (* translate the DummySet and RepeatedSet structures into lists of numbers: *)
    translateSet[DummySet[_, dums_, sym_]] := {Flatten[{dums}], sym}
    translateSet[RepeatedSet[lst_]]        := Flatten[{lst}]
    translateSet[sets_List]                :=
        translateSetStructure[{transpose[translateSet /@ Cases[sets, DummySet[_,{__},_]]],
                               translateSet /@ Cases[sets, _RepeatedSet]}]

        translateSetStructure[{{dumSets_, mQ_}, repSets_}] :=
            Sequence[Length /@ dumSets, Flatten[dumSets], mQ, Length /@ repSets, Flatten[repSets]]

        transpose[{}] := {{}, {}}
        transpose[x_] := Transpose[x]

linkMakePermGroup[GS_GenSet, len_Integer] :=
    Module[ {result},
        xpermConnect[];
        result = mTensor`xPermML`Private`MLMakePermGroup[toImagList[GS, len], len+2];
        fromImagList[#, len]& /@ Partition[result, len + 2]
    ]

